#include <stdio.h>

int main()
{
    printf("sizeof (char) = %ld\n", sizeof (char));
    printf("sizeof (int) = %ld\n", sizeof (int));
    printf("sizeof (long) = %ld\n", sizeof (long));
    printf("sizeof (float) = %ld\n", sizeof (float));
    printf("sizeof (double) = %ld\n", sizeof (double));
    printf("sizeof (char*) = %ld\n", sizeof (char*));
    printf("sizeof (int*) = %ld\n", sizeof (int*));
    printf("sizeof (long*) = %ld\n", sizeof (long*));
    printf("sizeof (float*) = %ld\n", sizeof (float*));
    printf("sizeof (double*) = %ld\n", sizeof (double*));
}
